package com.batcheleven.one;

public class WhileLoopStatement {
    public static void main(String[] args) {
        int a = 4;
        while (a<10){
            System.out.println(a);
            a++;
        }
        int number =4;
        do {
            System.out.print(number+"\t");
            number++;
        }while (number<7);

    }
}
